
public class apples {
	public static void main(String[] args){
		System.out.println(Math.abs(-1)); // removes -
		System.out.println(Math.ceil(7.4)); // rounds up
		System.out.println(Math.floor(7.8)); // rounds down
		
		System.out.println(Math.max(5, 8)); // max of two numbers
		System.out.println(Math.min(5, 8)); // min of two numbers
		
		System.out.println(Math.pow(2, 2)); // 2^2
		
		System.out.println(Math.sqrt(9)); // 9 square root
	}
}
